package br.com.calm.beans;

public class CursosExtras {
	private String nomeCertificado;
	private String nomeOrganizacao;
	private String dataEmissao;
	private String codigoCredencial;
	private String linkCertificado;
	
	public final String getNomeCertificado() {
		return nomeCertificado;
	}
	public final void setNomeCertificado(String nomeCertificado) {
		this.nomeCertificado = nomeCertificado;
	}
	public final String getNomeOrganizacao() {
		return nomeOrganizacao;
	}
	public final void setNomeOrganizacao(String nomeOrganizacao) {
		this.nomeOrganizacao = nomeOrganizacao;
	}
	public final String getDataEmissao() {
		return dataEmissao;
	}
	public final void setDataEmissao(String dataEmissao) {
		this.dataEmissao = dataEmissao;
	}
	public final String getCodigoCredencial() {
		return codigoCredencial;
	}
	public final void setCodigoCredencial(String codigoCredencial) {
		this.codigoCredencial = codigoCredencial;
	}
	public final String getLinkCertificado() {
		return linkCertificado;
	}
	public final void setLinkCertificado(String linkCertificado) {
		this.linkCertificado = linkCertificado;
	}
	
	

}
