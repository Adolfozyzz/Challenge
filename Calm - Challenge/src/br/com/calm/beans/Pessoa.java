package br.com.calm.beans;



public class Pessoa {
		private String id;
		private String nome;
		private String sexo;
		private int telefone;
		private  String dataNascimento;
		private String email;
		private String senha;
		public final String getId() {
			return id;
		}
		public final void setId(String id) {
			this.id = id;
		}
		public final String getNome() {
			return nome;
		}
		public final void setNome(String nome) {
			this.nome = nome;
		}
		public final String getSexo() {
			return sexo;
		}
		public final void setSexo(String sexo) {
			this.sexo = sexo;
		}
		public final int getTelefone() {
			return telefone;
		}
		public final void setTelefone(int telefone) {
			this.telefone = telefone;
		}
		public final String getDataNascimento() {
			return dataNascimento;
		}
		public final void setDataNascimento(String dataNascimento) {
			this.dataNascimento = dataNascimento;
		}
		public final String getEmail() {
			return email;
		}
		public final void setEmail(String email) {
			this.email = email;
		}
		public final String getSenha() {
			return senha;
		}
		public final void setSenha(String senha) {
			this.senha = senha;
		}
		
		
		
		
		
		
		

}
