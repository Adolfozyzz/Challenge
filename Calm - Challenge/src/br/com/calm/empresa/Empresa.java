package br.com.calm.empresa;

public class Empresa {
	private String cnpj;
	private int telComercial;
	private String idEmpresa;
	private String nomeEmpresa;
	public final String getCnpj() {
		return cnpj;
	}
	public final void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public final int getTelComercial() {
		return telComercial;
	}
	public final void setTelComercial(int telComercial) {
		this.telComercial = telComercial;
	}
	public final String getIdEmpresa() {
		return idEmpresa;
	}
	public final void setIdEmpresa(String idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	public final String getNomeEmpresa() {
		return nomeEmpresa;
	}
	public final void setNomeEmpresa(String nomeEmpresa) {
		this.nomeEmpresa = nomeEmpresa;
	}
	
	
	

}
